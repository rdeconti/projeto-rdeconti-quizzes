DROP TABLE CHARACTERS IF EXISTS;
CREATE TABLE CHARACTERS (
    ID                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME                  VARCHAR(30),
    TYPE                  VARCHAR(10),
    NUMBER_POINT_LIVE     INTEGER,
    NUMBER_POINT_STRENGTH INTEGER,
    NUMBER_POINT_DEFENSE  INTEGER,
    NUMBER_POINT_AGILITY  INTEGER,
    QUANTITY_DICE         INTEGER,
    QUANTITY_FACE_DICE    INTEGER);
CREATE INDEX CHARACTERS_NAME ON CHARACTERS (NAME);

DROP TABLE PLAYERS IF EXISTS;
CREATE TABLE PLAYERS (
    ID                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME                  VARCHAR(30),
    NUMBER_MATCHES        INTEGER,
    NUMBER_MATCHES_WON    INTEGER,
    NUMBER_MATCHES_LOST   INTEGER);
CREATE INDEX PLAYERS_NAME ON PLAYERS (NAME);

DROP TABLE SHIFTS IF EXISTS;
CREATE TABLE SHIFTS (
    ID                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_ID               INTEGER,
    SHIFT_NUMBER          INTEGER,
    ATTACKER              VARCHAR(30),
    ATTACK_POINTS         INTEGER,
    DEFENSE_POINTS        INTEGER,
    DAMAGE_POINTS         INTEGER,
    CHARACTER_LIVE_POINTS INTEGER,
    OPPONENT_LIVE_POINTS  INTEGER);
CREATE INDEX SHIFTS_GAME_ID ON SHIFTS (GAME_ID);
CREATE INDEX SHIFTS_ATTACKER ON SHIFTS (ATTACKER);

DROP TABLE GAMES IF EXISTS;
CREATE TABLE GAMES (
    ID                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_DATE             VARCHAR(10),
    GAME_TIME             VARCHAR(5),
    GAME_PLAYER_NAME      VARCHAR(30),
    GAME_CHARACTER_NAME   VARCHAR(30),
    GAME_OPPONENT_NAME    VARCHAR(30),
    GAME_INITIATIVE_NAME  VARCHAR(30));
CREATE INDEX GAME_PLAYER_NAME ON GAMES (GAME_PLAYER_NAME);
CREATE INDEX GAME_CHARACTER_NAME ON GAMES (GAME_CHARACTER_NAME);
CREATE INDEX GAME_OPPONENT_NAME ON GAMES (GAME_OPPONENT_NAME);
CREATE INDEX GAME_INITIATIVE_NAME ON GAMES (GAME_INITIATIVE_NAME);